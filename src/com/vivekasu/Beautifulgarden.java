package com.vivekasu;

public class Beautifulgarden {

    public static void main(String[] args){

    }


    public static int getCountofRemovals(int[] arr){

        int n = arr.length;
        int[] increase = new int[n];
        int[] decrease = new int[n];

        for(int i=0; i<n; i++){
            increase[i] = 1;
            decrease[i] = 1;

        }

        return 1;
    }
}
